export function FoodIcon({ food, iconKey, size = 34, className = "" }: Props) {
  const resolved = resolveFoodIconKey({ ...food, icon_key: iconKey ?? food?.icon_key })
  const assetPath = ICON_ASSETS[resolved]
  if (assetPath) {
    const baseScale = ICON_SCALE[resolved] ?? 0.92
    const transparentBoost = assetPath.includes("-transparent/") ? TRANSPARENT_ICON_BOOST : 1
    const scale = baseScale * transparentBoost
    const visualSize = Math.round(size * scale)

    return (
      <span
        className={`inline-flex shrink-0 items-center justify-center overflow-visible ${className}`}
        style={{
          width: size,
          height: size,
          filter: "drop-shadow(0 8px 14px rgba(0,0,0,0.44))",
        }}
        aria-hidden="true"
      >
        <Image
          src={assetPath}
          alt=""
          width={visualSize}
          height={visualSize}
          className="max-w-none object-contain"
        />
      </span>
    )
  }

  const palette = PALETTE[resolved] ?? PALETTE[FALLBACK_ICON_KEY]

  return (
    <span
      className={`inline-flex shrink-0 items-center justify-center overflow-hidden rounded-[0.9rem] border border-white/[0.07] shadow-[inset_0_1px_0_rgba(255,255,255,0.08)] ${className}`}
      style={{ width: size, height: size, background: `radial-gradient(circle at 32% 20%, rgba(255,255,255,0.13), transparent 34%), ${palette.bg}` }}
      aria-hidden="true"
    >
      <svg viewBox="0 0 60 60" width={size} height={size} role="img">
        <IconGlyph palette={palette} />
      </svg>
    </span>
  )
}
